fn generate_emoji_zwj(emoji_zwj_sequences: &Path, emoji_sequences: &Path) -> String {
    let mut sequences: BTreeMap<String, Vec<String>> = BTreeMap::new();

    for path in [emoji_zwj_sequences, emoji_sequences] {
        for line in fs::read_to_string(path)
            .unwrap_or_else(|err| panic!("read {}: {err}", path.display()))
            .lines()
        {
            if !starts_with_hex(line)
                || !(line.contains("; RGI_Emoji_ZWJ_Sequence")
                    || line.contains("; RGI_Emoji_Modifier_Sequence"))
            {
                continue;
            }
            let raw = line.split(';').next().unwrap_or_default().trim();
            let parts = raw.split_whitespace().collect::<Vec<_>>();
            if parts.is_empty() {
                continue;
            }
            let base = parts[0].to_string();
            let seq = parts
                .iter()
                .map(|part| format!("\\N{{U+{part}}}"))
                .collect::<String>();
            sequences
                .entry(base)
                .or_default()
                .push(format!("\"{seq}\""));
        }
    }

    let trigger_codepoints = [
        "261D", "26F9", "270C", "270D", "2764", "1F3CB", "1F3CC", "1F3F3", "1F3F4", "1F441",
        "1F574", "1F575", "1F590", "20E3",
    ];

    let mut out = String::new();
    out.push_str(
        ";;; emoji-zwj.el --- emoji zwj character composition table  -*- lexical-binding:t -*-\n",
    );
    out.push_str(";;; Automatically generated from admin/unidata/emoji-{zwj-,}sequences.txt\n");
    out.push_str("(eval-when-compile (require 'regexp-opt))\n");
    out.push_str("(setq auto-composition-emoji-eligible-codepoints\n");
    out.push_str("'(\n");
    for codepoint in trigger_codepoints {
        let _ = writeln!(out, "?\\N{{U+{codepoint}}}");
    }
    out.push_str("))\n");
    out.push_str("(dolist (elt (eval-when-compile `(\n");
    for (base, seqs) in &sequences {
        let _ = writeln!(out, "(#x{base} .");
        out.push_str(",(regexp-opt\n");
        out.push_str("'(\n");
        for seq in seqs {
            let _ = writeln!(out, "{seq}");
        }
        let _ = writeln!(out, "\"\\N{{U+{base}}}\\N{{U+FE0E}}\"");
        let _ = writeln!(out, "\"\\N{{U+{base}}}\\N{{U+FE0F}}\"");
        out.push_str(")))\n");
    }
    out.push_str(")))\n");
    out.push_str("  (set-char-table-range composition-function-table\n");
    out.push_str("                        (car elt)\n");
    out.push_str(
        "                        (nconc (char-table-range composition-function-table (car elt))\n",
    );
    out.push_str("                               (list (vector (cdr elt)\n");
    out.push_str("                                             0\n");
    out.push_str(
        "                                             #'compose-gstring-for-graphic)))))\n",
    );
    out.push('\n');
    out.push_str(";; The following two blocks are derived by hand from emoji-sequences.txt\n");
    out.push_str(
        ";; FIXME: add support for Emoji_Keycap_Sequence once we learn how to respect FE0F/VS-16\n",
    );
    out.push_str(";; for ASCII characters.\n");
    out.push('\n');
    out.push_str(";; Flags\n");
    out.push_str("(set-char-table-range composition-function-table\n");
    out.push_str("                      '(#x1F1E6 . #x1F1FF)\n");
    out.push_str("                      (nconc (char-table-range composition-function-table '(#x1F1E6 . #x1F1FF))\n");
    out.push_str("                             (list (vector \"[\\\\U0001F1E6-\\\\U0001F1FF][\\\\U0001F1E6-\\\\U0001F1FF]\"\n");
    out.push_str("                                           0\n");
    out.push_str("                                           #'compose-gstring-for-graphic))))\n");
    out.push('\n');
    out.push_str(";; UK Flags\n");
    out.push_str("(set-char-table-range composition-function-table\n");
    out.push_str("                      #x1F3F4\n");
    out.push_str(
        "                      (nconc (char-table-range composition-function-table #x1F3F4)\n",
    );
    out.push_str("                             (list (vector \"\\\\U0001F3F4\\\\U000E0067\\\\U000E0062\\\\(?:\\\\U000E0065\\\\U000E006E\\\\U000E0067\\\\|\\\\U000E0073\\\\U000E0063\\\\U000E0074\\\\|\\\\U000E0077\\\\U000E006C\\\\U000E0073\\\\)\\\\U000E007F\"\n");
    out.push_str("                                           0\n");
    out.push_str("                                           #'compose-gstring-for-graphic))))\n");
    out.push('\n');
    out.push_str("(provide 'emoji-zwj)\n");
    out
}

fn parse_hex(value: &str) -> u32 {
    u32::from_str_radix(value, 16).unwrap_or_else(|err| panic!("invalid hex {value}: {err}"))
}

fn starts_with_hex(line: &str) -> bool {
    line.as_bytes()
        .first()
        .is_some_and(|b| (*b as char).is_ascii_hexdigit())
}

fn contains_any(haystack: &str, needles: &[&str]) -> bool {
    needles.iter().any(|needle| haystack.contains(needle))
}

fn earliest_match(haystack: &str, needles: &[&str]) -> Option<usize> {
    needles
        .iter()
        .filter_map(|needle| haystack.find(needle))
        .min()
}
